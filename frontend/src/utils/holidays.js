function pad(n) {
  return String(n).padStart(2, '0')
}

export function getHoliday(date) {
  const key = `${date.getFullYear()}-${pad(date.getMonth() + 1)}-${pad(date.getDate())}`
  return HOLIDAYS[key] ?? null
}
